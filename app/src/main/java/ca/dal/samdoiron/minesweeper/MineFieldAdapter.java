package ca.dal.samdoiron.minesweeper;

import android.content.Context;
import android.view.View;
import android.view.ViewGroup;
import android.widget.BaseAdapter;
import android.widget.GridView;
import android.widget.ImageView;

public class MineFieldAdapter extends BaseAdapter {
    Context context;

    public MineFieldAdapter(Context c) {
        context = context;
    }

    @Override
    public int getCount() {
        return 0;
    }

    @Override
    public Object getItem(int i) {
        return null;
    }

    @Override
    public long getItemId(int i) {
        return 0;
    }

    @Override
    public View getView(int i, View recycled, ViewGroup viewGroup) {
        ImageView imageView;
        if (recycled == null) {
            imageView = new ImageView(context);
            imageView.setLayoutParams(new GridView.LayoutParams(85, 85));
            imageView.setScaleType(ImageView.ScaleType.CENTER_CROP);
            imageView.setPadding(8, 8, 8, 8);
        } else {
            imageView = (ImageView)recycled;
        }

        imageView.setImageResource(R.drawable.bomb);
        return imageView;
    }
}
